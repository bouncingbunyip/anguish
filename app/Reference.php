    public function substituteSingleQuote($substitute = null) {
        $contractions = $this->getContractions();
        if (is_null($substitute)) {
            $substitute = "@~@";
        }

        $retval = str_replace("'", $substitute, $contractions);
        return $retval;
    }
    
